def test_compliance_checking():
    """Test 8: Compliance checking example"""
    print("=" * 70)
    print("TEST 8: Regulatory Compliance Check")
    print("=" * 70)

    output_file = "test1_output.out"
    results = parse_aermod_output(output_file)

    # Example NAAQS standards (hypothetical)
    standards = {
        'ANNUAL': 12.0,  # Annual PM2.5 standard
        '24HR': 35.0,    # 24-hour PM2.5 standard
        '1HR': 300.0     # Example 1-hour standard
    }

    print("Compliance Check (PM2.5 Standards):\n")

    for period, standard in standards.items():
        if period in results.concentrations:
            max_info = results.get_max_concentration(period)
            max_conc = max_info['value']

            exceeds = max_conc > standard
            status = "EXCEEDS" if exceeds else "COMPLIES"
            pct = (max_conc / standard) * 100

            print(f"{period}:")
            print(f"  Standard:    {standard:.2f} ug/m^3")
            print(f"  Maximum:     {max_conc:.2f} ug/m^3")
            print(f"  Percentage:  {pct:.1f}% of standard")
            print(f"  Status:      {status}")
            print()


