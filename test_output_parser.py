def test_point_lookup():
    """Test 4: Look up concentration at specific points"""
    print("=" * 70)
    print("TEST 4: Point Concentration Lookup")
    print("=" * 70)

    output_file = "test1_output.out"
    results = parse_aermod_output(output_file)

    # Look up concentrations at specific points
    test_points = [
        (500.0, 0.0),
        (100.0, 100.0),
        (200.0, 0.0)
    ]

    print("Annual concentrations at test points:")
    for x, y in test_points:
        conc = results.get_concentration_at_point(x, y, 'ANNUAL', tolerance=1.0)
        if conc is not None:
            print(f"  ({x:6.1f}, {y:6.1f}): {conc:8.4f} ug/m^3")
        else:
            print(f"  ({x:6.1f}, {y:6.1f}): No receptor found")
    print()


